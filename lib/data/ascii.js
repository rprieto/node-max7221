var bitmap = require('../bitmap');

module.exports = {};

function process(symbols, data) {
    symbols.forEach(function(symbol, index) {
        var verticalSlice = [];
        for (var i = 0; i < 8; ++i) {
            verticalSlice.push(data[i * symbols.length + index]);
        }
        module.exports[symbol] = bitmap.fromStrings(verticalSlice);
    });
}

